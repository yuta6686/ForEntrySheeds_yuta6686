#include "main.h"
#include "credit.h"
#include "input.h"
#include "texture.h"
#include "sprite.h"
#include "fade.h"
#include <cmath>


//*****************************************************************************
// マクロ定義
//*****************************************************************************

#define SCREEN_WIDTH	(1920 )				// ウインドウの幅
#define SCREEN_HEIGHT	(1080 )				// ウインドウの高さ

#define SCEREN_WIDTH_HURF SCREEN_WIDTH / 2
#define SCEREN_HEIGHT_HURF SCREEN_HEIGHT / 2

//*****************************************************************************
// プロトタイプ宣言
//*****************************************************************************


//*****************************************************************************
// グローバル変数
//*****************************************************************************
static int	g_TextureNo;	// テクスチャ情報



//=============================================================================
// 初期化処理
//=============================================================================
HRESULT InitCredit(void)
{
    g_TextureNo= LoadTexture("data/TEXTURE/credit.png");

    return E_NOTIMPL;
}



//=============================================================================
// 終了処理
//=============================================================================
void UninitCredit(void)
{
}



//=============================================================================
// 更新処理
//=============================================================================
void UpdateCredit(void)
{
    if (GetKeyboardTrigger(DIK_RETURN) || IsMouseLeftTriggered() && GetFadeState()==FADE_NONE)
    {
        SceneTransition(SCENE_TITLE);
    }

}



//=============================================================================
// 描画処理
//=============================================================================
void DrawCredit(void)
{
    DrawSpriteColor(g_TextureNo, SCEREN_WIDTH_HURF, SCEREN_HEIGHT_HURF,
                    SCREEN_WIDTH, SCREEN_HEIGHT,
                    0.0f, 0.0f, 1.0f, 1.0f,
                    D3DXCOLOR(1.0f, 1.0f, 1.0f, 1.0f));
}


